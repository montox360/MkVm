<p>Derecho de Autor: conjunto de normas que regulan los derechos morales y patrimoniales que la ley concede a los autores y coautores por la creación de una obra literaria, artística o científica, que incluyen programas de computación o didáctica, pública o inédita.</p>
<p>&nbsp;</p>
<p><strong>LEGISLACIÓN</strong>:</p>
<p>La LEY SOBRE EL DERECHO DE AUTOR en Venezuela, data del 14 de agosto de 1993, está compuesta por XI títulos y 145 artículos.</p>
<p>&nbsp;</p>
<p><strong>OFICINA PÚBLICA DE REGISTRO</strong>:</p>
<p>La oficina nacional de registro en Venezuela es El Servicio Autónomo de Propiedad Intelectual (SAPI) en el Dpto. de Derecho de Autor.</p>
<p>&nbsp;</p>
<p><strong>¿CÓMO TRABAJAMOS? </strong></p>
<p>Contamos con un equipo de especialistas encargado de llevar a cabo pre avalúos de los proyectos a registrar, a fin de determinar su valor y el área al cual pertenece el proyecto, de acuerdo a la materia que trata. Adicionalmente, trabajamos en función de cumplir todos los requisitos exigidos por la Ley para asegurar el éxito del trámite.</p>
