<p>&nbsp;</p>
<p>Diseños Industriales: Se basan exclusivamente en la forma de la invención. Se refieren sobre todo a la apariencia de la totalidad o de parte de un producto, no se basa específicamente en su funcionalidad o proceso, sino que busca resaltar la apariencia física. La novedad viene dada por la apariencia del producto en si.</p>
